#ask the user to enter 1,2,or 3. If 1 print 'Thank you', If 2 print 'Well done', If 3 print 'Correct'
#otherwise print Error message

number = input('Please enter 1, 2, or 3: ')

if (number == "1") :
    print('Thank you')
elif (number == "2") :
    print('Well done')
elif (number == "3") :
    print('Correct')
else :
    print('Error message')